/*********************************************************************
 *	function name:  usart3_recv_idle
 *	describe: Receiving data in DMA idle mode
 *	input:
 *	output:
 *	return:null
 *	revision histroy :   1.
 *                      2.
 **********************************************************************/
void usart1_recv_idle(UART_HandleTypeDef *huart)
{
    uint16_t i = I_ZERO;

    if((__HAL_UART_GET_FLAG(huart,UART_FLAG_IDLE) != RESET))  
    {
        if(huart->Instance == LPUART1)
        {
            __HAL_UART_CLEAR_IDLEFLAG(huart);
            i = huart->Instance->ISR;
            i = huart->Instance->RDR;
            i = hdma_lpuart_rx.Instance->CNDTR; 

            //HAL_UART_DMAStop(huart);
            HAL_UART_Abort(huart);
            if(g_usart1_rx_finish == 0)
            {
                memcpy(ar_usart1_tx_buffer,ar_usart1_rx_buffer,(MAX_RECEIVE_DATA - i));
                g_usart1_tx_len = MAX_RECEIVE_DATA - i;
                g_usart1_rx_finish = 1;
            }
            memset(ar_usart1_rx_buffer,0x00,MAX_RECEIVE_DATA);
            HAL_UART_Receive_DMA(huart,(uint8_t *)&ar_usart1_rx_buffer,MAX_RECEIVE_DATA);
        }
    }
}

